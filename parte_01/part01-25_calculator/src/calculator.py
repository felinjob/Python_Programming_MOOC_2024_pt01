number_1 = int(input("Number 1: "))
number_2 = int(input("Number 2: "))
operator = input("Operation: ")

if operator == "add":
    print()
    print(f"{number_1} + {number_2} = {number_1 + number_2}")
elif operator == "subtract":
    print()
    print(f"{number_1} - {number_2} = {number_1 - number_2}")
elif operator == "multiply":
    print()
    print(f"{number_1} * {number_2} = {number_1 * number_2}")